export const getRowTitles = () => {
  const rowCount = 16
  const charCodeStartAt = 65
  const charCodeEndAt = charCodeStartAt + rowCount - 1

  const rowTitles = []

  for (let i = charCodeStartAt; i <= charCodeEndAt; i++) {
    rowTitles.push(String.fromCharCode(i))
  }

  return rowTitles
}

export const getColTitles = () => {
  const colCount = 30

  const colTitles = []

  for (let i = 1; i <= colCount; i++) {
    colTitles.push(`${i}`)
  }

  return colTitles
}

